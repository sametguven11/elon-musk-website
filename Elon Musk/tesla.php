         <h1>Spor elektrikli otomobil: Tesla Model S:</h1>
         <p>Tesla Model S, bir Jaguar'da bulabileceğiniz benzerlikte, uzun ve alçak tasarım çizgilere sahip, son derece donanımlı bir sedan modeldir.</p>
         <p>Model S önceki sürümlerinden bazılarında arkadan çekiş prensibiyle çalışırken, son üretilen Model S’lerin tümü dört tekerlekten çekişli olarak çalışıyor. Bu da Model S’i performans açısından daha da güçlü kılıyor. Performans konusunda fazlasıyla iyi olan Model S’in Plaid versiyonu inanması güç olsa da tam 1020 beygir gücünde.</p>
         <p>Model S ilk olarak 2012’de piyasaya sürüldü ve Tesla’nın bir otomobil üreticisi olarak piyasaya girmesine yardımcı oldu. Tesla, görücüye çıktığından beri Model S’yi güncellemeye devam etti ve son olarak şuan Model S’in üç versiyonu bulunuyor: Long Range Plus; Ekose; ve Plaid+. Standart ve Performans modelleri kısa süre önce ürün gamından çıkarıldı.</p>
