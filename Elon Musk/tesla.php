          <h1>Şahin kanatları ile elektrikli SUV Tesla Model X:</h1>
          <p>Tesla Model X için elektrikli araçların zirvesi ifadesini kullanabiliriz. Sahip olduğu özellikler ve fiyatı da bu söylemin doğruluğunu kanıtlar niteliğinde. 7 kişilik oturma kapasitesine sahip elektrikli otomotiv Tesla Model X tasarımın büyük bir kısmını(özellikle iç tasarımını)  Model S’e borçlu.</p>
          <p>Aracın tartışmasız en dikkat çeken özelliklerinden biri de “falcon wings” diye adlandırılan yukarıya açılır elektronik kapıları. Tesla’nın tasarımcıları bu kapıları,1985 yılında vizyona giren Amerikan bilim kurgu filmi Geleceğe Dönüş’ten esinlenerek tasarladıklarını belirtiyor.</p>
          <p>Tesla’nın özellikle üzerinde durduğu tam otonom sürüş teknolojisini kullanmak istiyorsanız her modelde olduğu gibi bu modelde de ek ücret ödemeniz gerekyior. Eğer Model X otomobilinizde tam otonom bir sürüş keyfi sürmek istiyorsanız 10 bin dolar daha ödeme yapmanız gerekiyor. Ancak otomatik pilot özelliği için ücret ödemiyorsunuz Tesla bu özelliği standart olarak sunuyor. </p>
