         <h1>Devlerle yarışan otomobil ve SUV hibriti: Tesla Model Y</h1>
         <p>Tesla Model Y tam anlamıyla Tesla’nın kompakt SUV’u olarak geçiyor. Çünkü diğer SUV model olan Model X’e göre Model Y hem daha küçük ebatlarda hem de daha ekonomik. Yani aslında Model Y Tesla’nın en ucuz modeli Model 3 ile en donanımlı aracı Model X arasındaki boşluğu dolduruyor. Aynı zamanda dilerseniz 3 bin dolar daha ödeyerek aracınıza 2 koltuk daha ekleyerek kapasiteyi 7 kişiye çıkarabilirsiniz.</p>
         <p>Kompakt SUV segmenti günümüzde en çok satan segmentlerdendir ve elektrikli seçeneği yok denecek kadar azdır. Bu yüzden Tesla Model Y ile elini rakiplerine karşı bir hayli güçlendirmiştir. Model X ve Model S’ye göre daha yavaş bir otomobil olsa da performans versiyon Model Y’nin 0-100 performansı, Bentley’in SUV modeli 626 beygir gücünde Bentayga’dan bile daha iyi. Tesla Model Y’nin batarya menzili ve 0-100km/s süresi işte şu şekilde:</p>
