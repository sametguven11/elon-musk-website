         <h1>Tesla'nın Kuruluşu</h1>
         <p>Tesla, eski adıyla “Tesla Motors” 2003 yılında iki Amerikalı girişimci olan Martin Eberhard ve Marc Tarpenning tarafından kurulmuştur. Tesla Motors isimli bu şirket 2017 yılında Tesla adını almıştır. Tesla şirketinin temel amacı elektrikli otomobil üretmektir ve 16 yıldır bu alanda çok büyük adımlar atmıştır. Tesla şirketi adını Nikola Tesla’dan almıştır.
        Tesla şirketi kurulduğunda amaçları elektrikli spor araba üretmekti. Martin Eberhard ve Marc Tarpenning bu şirket için yatırım arayışına girmişlerdi. Marc Tarpenning mali işlerden sorumluydu ve Martin Eberhand ise yöneticisiydi. Çeşitli kaynaklardan yatırım elde eden Tesla şirketi, en büyük yatırımını Paypal’ın kurucu ortaklarından biri olan Elon Musk’dan almıştır. 2004 yılında yapılan bu yatırımın değeri 30 milyon dolar kadardı ve bu yatırımdan sonra Elon Musk, şirketin başına geçmiştir.</p>
